## 5 main datatypes (called atomic data types) ##

# Integer
x <- 2L
x
typeof(x)
# RStudio automatically stores numbers as doubles unless you specify L at the end
x2 <- 2
x2
typeof(x2)


# Double
y <- 2.5
y
typeof(y)

# Complex
z <- 3 + 2i
z
typeof(z)

#  Character
a <- 'h'
a
typeof(a)

# Logical
q <- T
q
typeof(q)


